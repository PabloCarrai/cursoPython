""" 
Probar extraer porciones de una lista
"""


lista = [0, 1, 2, 3, 4, 5, 6]
lista2 = lista[2:5]  # desde posicion2 hasta 5 sin incluir
print(lista2)
lista3 = lista[:5]  # todo hasta el 5 sin incluir
print(lista3)
lista4 = lista[3:]  # desde 3 hasta el final sin incluir
print(lista4)
tupla = (0, 1, 2, 3, 4)
print(tupla[2:])
cadena = "administracion"
print(cadena[2:5])
print(cadena[2:])
