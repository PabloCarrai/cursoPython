""" 
Escribir un programa en el cual se ingresen cuatro numeros. 
Calcular e informar la suma de los dos primeros
y el producto del tercero y cuarto
"""
numero = int(input("Ingrese primer numero "))
numero1 = int(input("Ingrese segundo numero "))
numero2 = int(input("Ingrese tercer numero "))
numero3 = int(input("Ingrese cuarto numero "))
suma = numero+numero1
producto = numero2*numero3
print("La suma es ")
print(suma)
print("El producto es ")
print(producto)
