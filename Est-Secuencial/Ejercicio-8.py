""" 
Calcular el sueldo mensual de un operario
conociendo la cantidad de horas trabajadas 
y el valor por hora
"""
cantidadHoras = int(input("Ingrese la cantidad de horas trabajadas "))
valorHora = int(input("Ingrese el valor de la hora trabajada "))
sueldo = cantidadHoras*valorHora
print("El sueldo es ")
print(sueldo)
